def full_counting_sort_prep(array)
  counter = Array.new(100, 0)
  array.each do |val|
    val = val.split(' ')[0].to_i
    counter[val] += 1
  end
  sum = 0
  aux = []
  counter.each_with_index do |elem, idx|
    sum = counter[0..idx].reduce(:+)
    aux << sum
  end
  aux

end

